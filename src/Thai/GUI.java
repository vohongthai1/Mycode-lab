    private void tblHienThiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHienThiMouseClicked
        // TODO add your handling code here:
        int clickedRow = tblHienThi.getSelectedRow();
    if (clickedRow >= 0) {
        ArrayList<NhanVien> employees = a.getA();
        NhanVien y = employees.get(clickedRow);

        txtMa.setText(y.getMaNhanVien());
        txtHoTen.setText(y.getHoTen());
        txtSDT.setText(y.getSoDienThoai());
        txtEmail.setText(y.getMail());
        txtNgaySinh.setText(String.valueOf(y.getNgaySinh()));
        if (y.isGioiTinh()) {
            rbtNam.setSelected(true);
        } else {
            rbtNu.setSelected(true);
        }
        txtLuongCoBan.setText(String.valueOf(y.getLuongCoBan()));

        if (y instanceof LapTrinhVien) {
            rbtLapTrinhVien.setSelected(true);
            lblChung.setText("Số giờ tăng ca");
            txtChung.setText(String.valueOf(((LapTrinhVien) y).getSoGioTangCa()));
            txtChung.setVisible(true);
        } else if (y instanceof KiemChungVien) {
            rbtKiemChungVien.setSelected(true);
            lblChung.setText("Số lỗi phát hiện");
            txtChung.setText(String.valueOf(((KiemChungVien) y).getSoLoiPhatHien()));
            txtChung.setVisible(true);
        }
    }
    }//GEN-LAST:event_tblHienThiMouseClicked

