    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        
        StringBuilder sb = new StringBuilder();
        if (txtMa.getText().equals("")) {
            txtMa.setBackground(Color.red);
            sb.append("Mã nhân viên không được để trống\n");
        } else {
            txtMa.setBackground(Color.white);
        }
        if ((txtHoTen).getText().equals("")) {
            txtHoTen.setBackground(Color.red);
            sb.append("Họ tên nhân viên không được để trống\n");
        } else {
            txtHoTen.setBackground(Color.white);
        }
        if (txtSDT.getText().equals("")) {
            txtSDT.setBackground(Color.red);
            sb.append("SĐT nhân viên không được để trống\n");
        } else {
            txtSDT.setBackground(Color.white);
        }
        if (txtNgaySinh.getText().equals("")) {
            txtNgaySinh.setBackground(Color.red);
            sb.append("Ngày sinh nhân viên không được để trống\n");
        } else {
            txtNgaySinh.setBackground(Color.white);
        }
        if (txtLuongCoBan.getText().equals("")) {
            txtLuongCoBan.setBackground(Color.red);
            sb.append("Lương cơ bản nhân viên không được để trống\n");
        } else {
            txtLuongCoBan.setBackground(Color.white);
        }
        if (rbtLapTrinhVien.isSelected()) {
            if (txtChung.getText().equals("")) {
                txtChung.setBackground(Color.red);
                sb.append("Số giờ overtime nhân viên không được để trống\n");
            } else {
                txtChung.setBackground(Color.white);
            }

        } else if (rbtKiemChungVien.isSelected()) {
            if (txtChung.getText().equals("")) {
                txtChung.setBackground(Color.red);
                sb.append("Số lỗi nhân viên không được để trống\n");
            } else {
                txtChung.setBackground(Color.white);
            }

        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(null, "Thông tin nhân viên không được để trống", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String ma = txtMa.getText();
        String ten = txtHoTen.getText();
        String sdt = txtSDT.getText();
        String mail = txtEmail.getText();
        Date nt = new Date(txtNgaySinh.getText());
        boolean gioiTinh = rbtNam.isSelected();
        double luongCoBan = Double.parseDouble(txtLuongCoBan.getText());

        NhanVien x = null;

        if (rbtLapTrinhVien.isSelected()) {
            int soGioTangCa = Integer.parseInt(txtChung.getText());
            x = new LapTrinhVien(soGioTangCa, ma, ten, sdt, mail, nt, gioiTinh, luongCoBan);
        } else if (rbtKiemChungVien.isSelected()) {
            int soLoiPhatHien = Integer.parseInt(txtChung.getText());
            x = new KiemChungVien(soLoiPhatHien, ma, ten, sdt, mail, nt, gioiTinh, luongCoBan);
        }

        if (x != null && a.them(x)) {
            loadData(a.getA());
            JOptionPane.showMessageDialog(null, "Thêm thành công!");
            lblHienThi.setText("Bạn vừa thêm " + ma + " vào danh sách");
        } else {
            JOptionPane.showMessageDialog(null, "Đã có " + ma + " này trong danh mục");
        }

        ClearForm();
    }//GEN-LAST:event_btnThemActionPerformed

