    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int xoa = tblHienThi.getSelectedRow();

        if (xoa == -1) {
            JOptionPane.showMessageDialog(null, "Hãy chọn 1 đối tượng cần xóa");
        } else {
            int luachon = JOptionPane.showConfirmDialog(null, "Bạn muốn xóa?");
            if (luachon == JOptionPane.YES_OPTION) {
                // Xóa hàng được chọn từ DefaultTableModel (được gán cho dtm)
                DefaultTableModel model = (DefaultTableModel) tblHienThi.getModel();
                model.removeRow(xoa); // Xóa hàng tại chỉ mục xoa

                JOptionPane.showMessageDialog(null, "Đã xóa thành công");

                // Cập nhật dữ liệu trong ArrayList hoặc danh sách khác nếu cần
                // Ví dụ: a là đối tượng chứa danh sách NhanVien
                ArrayList<NhanVien> x = a.getA();
                if (xoa < x.size()) {
                    x.remove(xoa);
                    a.setA(x); // Cập nhật danh sách sau khi xóa
                }

                ClearForm();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

