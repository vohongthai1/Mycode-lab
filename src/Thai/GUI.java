    public void loadData(ArrayList<NhanVien> b){
         while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        for (NhanVien x : b) {
            if (x instanceof LapTrinhVien) {
                dtm.addRow(new Object[]{"Lập trình",x.getMaNhanVien(),x.getHoTen(),x.getSoDienThoai(),x.getMail(),x.getNgaySinh(),(x.isGioiTinh()==true?"Nam":"Nữ"),x.getLuongCoBan(),((LapTrinhVien) x).getSoGioTangCa(),"",x.getLuong()});
            }
            if (x instanceof KiemChungVien) {
                dtm.addRow(new Object[]{"Kiểm chứng",x.getMaNhanVien(),x.getHoTen(),x.getSoDienThoai(),x.getMail(),x.getNgaySinh(),(x.isGioiTinh()==true?"Nam":"Nữ"),x.getLuongCoBan(),"",((KiemChungVien) x).getSoLoiPhatHien(),x.getLuong()});
            }
        }
    }
